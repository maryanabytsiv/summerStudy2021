package com;

public class SmartClass {

    public Integer code;
    public String name;

    public SmartClass(Integer code, String name) {
        this.code = code;
        this.name = name;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public void setName(String name) {
        this.name = name;
    }

}
